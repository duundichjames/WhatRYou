## ===============================

## =============================== <4>
N = c(10, 100, 1000, 10000)
결과 <- list()
for(n in N) {
  set.seed(1234)
  결과[[as.character(n)]] <- sample(박스, n, replace = TRUE)
}
## ===============================

## =============================== <5>
str(결과)
## ===============================

## =============================== <6>
흰공비율 <- function(x) {
  length(x[x == "흰공"])/length(x)
}
흰공비율산출 <- sapply(결과, 흰공비율)
## ===============================

## =============================== <7>
print(흰공비율산출)
## ===============================

## =============================== <8>
흰공비율차이 = 흰공비율산출 - 0.3
